                    <p>
El término cosmético se creó en el siglo XVII a partir de la palabra griega kosmetikós, que significa "relativo a la ornamentación".1 La primera prueba arqueológica del uso de cosméticos se encontró en el Antiguo Egipto, alrededor del año 4000 a. C. Se sabe que los antiguos griegos y romanos también usaban cosméticos.</p>
                    <p>La leche de burra
La leche de burra se usaba no sólo para restablecer el pulmón sino también para dar frescura a la piel. En el libro XXVIII de su Historia natural, Plinio el Viejo menciona la leche de burra como excelente cosmético y cita la superstición de algunas mujeres que se lavaban la cara con ella todos los días muchas veces. En el libro XI afirma que Popea llevaba en sus viajes de una recua de quinientas burras para poder bañarse en su leche, algo que también mencionan otros autores.</p>
