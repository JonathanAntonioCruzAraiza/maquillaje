                    <p>Los cosméticos (también llamados maquillaje) son productos que se utilizan para la higiene corporal o para mejorar la apariencia, especialmente del rostro. Por lo general son mezclas de compuestos químicos, algunos se derivan de fuentes naturales, muchos otros son sintéticos.</p>

<p> En los Estados Unidos, la Administración de Alimentos y Medicamentos (FDA), que regula los cosméticos, define los cosméticos como "sustancia destinada a ser aplicada al cuerpo humano para limpiar, embellecer o alterar la apariencia sin afectar la estructura del cuerpo o funciones". Esta amplia definición también incluye cualquier material destinado para su uso como un componente de un producto cosmético. La FDA excluye específicamente el jabón de esta categoría.</p>
